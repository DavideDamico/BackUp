// Operatore di assegnamento (=)
let x = 5; // Assegna il valore 5 alla variabile x

// Operatori aritmetici (+, -, *, /, %)
let a = 10 + 5; // Somma: a = 15
let b = 10 - 5; // Sottrazione: b = 5
let c = 10 * 5; // Moltiplicazione: c = 50
let d = 10 / 5; // Divisione: d = 2
let e = 10 % 3; // Resto della divisione: e = 1

// Operatori di incremento/decremento (++, --)
let num = 10;
num++; // Incremento: num = 11
num--; // Decremento: num = 10

// Operatore di concatenazione per stringhe (+)
let stringa1 = "Ciao, ";
let stringa2 = "mondo!";
let saluto = stringa1 + stringa2; // Concatenazione: saluto = "Ciao, mondo!"

// Operatore di confronto (==, ===, !=, !==, >, <, >=, <=)
let valore1 = 5;
let valore2 = "5";
valore1 == valore2; // Uguaglianza non strettamente controllata: true
valore1 === valore2; // Uguaglianza strettamente controllata: false

let numero = 10;
let stringaNumero = "10";

numero != stringaNumero; // Non uguale non strettamente controllato: false
numero !== stringaNumero; // Non uguale strettamente controllato: true

let valoreA = 20;
let valoreB = 15;

valoreA > valoreB; // Maggiore di: true
valoreA < valoreB; // Minore di: false
valoreA >= valoreB; // Maggiore o uguale a: true
valoreA <= valoreB; // Minore o uguale a: false

// Operatore logico (&&, ||, !)
let condizione1 = true;
let condizione2 = false;
let risultatoAnd = condizione1 && condizione2; // AND logico: risultatoAnd = false
let risultatoOr = condizione1 || condizione2; // OR logico: risultatoOr = true
let risultatoNot = !condizione1; // NOT logico: risultatoNot = false